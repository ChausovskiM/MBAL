import math
def Ai_PR(P, T, i, Pc_i, Tc_i, w_i):
    """
    Расчёт индивидуального параметра Ai для компонента i
    в уравнении состояния Пинг–Робинсона.

    Параметры:
    - P (float): давление, Па
    - T (float): температура, K
    - i (int): индекс компонента (информационный)
    - Pc_i (float): критическое давление, МПа
    - Tc_i (float): критическая температура, K
    - w_i (float): ацентрический фактор (безразмерный)

    Возвращает:
    - Ai (float): индивидуальный параметр привлекательности, безразмерный
    """
    Pkr = Pc_i * 1e6  # Перевод критического давления из МПа в Па
    Tkr = Tc_i
    w = w_i

    Ppr = P / Pkr
    Tpr = T / Tkr

    m = 0.37464 + 1.54226 * w - 0.2669 * w ** 2
    alpha = (1 + m * (1 - math.sqrt(Tpr))) ** 2

    Ai = 0.457235529 * alpha * (Ppr / Tpr ** 2)
    return Ai

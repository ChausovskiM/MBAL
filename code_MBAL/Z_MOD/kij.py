import math

def kij(i, j, Vc_i, Vc_j):
    """
    Расчёт коэффициента взаимодействия kij между компонентами i и j
    по их критическим объёмам Vc.

    Параметры:
    - i (int): индекс первого компонента
    - j (int): индекс второго компонента
    - Vc_i (float): критический объём компонента i (см³/моль)
    - Vc_j (float): критический объём компонента j (см³/моль)

    Возвращает:
    - kij (float): коэффициент взаимодействия (безразмерный)
    """

    if Vc_i == 0 or Vc_j == 0:
        return 0.0  # можно также выбросить исключение

    root_i = Vc_i ** (1 / 3)
    root_j = Vc_j ** (1 / 3)

    return 1 - 2 * math.sqrt(root_i * root_j) / (root_i + root_j)
